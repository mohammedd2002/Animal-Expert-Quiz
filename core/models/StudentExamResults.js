class StudentExamResults {
    id;
    examId;
    studentId;
    score;
    dateTaken;

    constructor(id, examId, studentId, score, dateTaken) {

        this.id = id;
        this.examId = examId;
        this.studentId = studentId;
        this.score = score;
        this.dateTaken = dateTaken;


    }   
}